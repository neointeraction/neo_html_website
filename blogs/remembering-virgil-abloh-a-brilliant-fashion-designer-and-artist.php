                    The Abloh Style
                </h4>

                <p class="blog-body-text">
                    In 2009 Kenzo began his remarkable career with the opening of a retail store known as the RSVP
                    Gallery and partnering with Kanye West. In 2013, Abloh founded his first fashion house with the
                    high-end streetwear brand Off-White. He also partnered up with IKEA to design furniture for
                    apartments and houses as well as an easy-to-carry tote. Abloh worked towards fulfilling his vision
                    for the IKEA collection by sketching out drafts of generic pieces of furniture while adding his own
                    aesthetics to the designs by using a doorstop to level out furniture items. In 2017, he was asked to
                    design a new collection in conjunction with Nike entitled "The Ten" and he re-designed a variety of
                    the company's best-selling shoes. On March 25, 2018, he was named the artistic director of Louis
                    Vuitton's menswear ready-wear line, making him the first person of African descent to lead the
                    brand's menswear line, as well as one of the few black designers at the helm of a major French
                    fashion house.
