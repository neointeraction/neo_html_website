                <p class="blog-body-text">
                    <b>4. Seamless Cross-Platform Experiences</b>
                </p>
                <p class="blog-body-text">
                    People are nowadays on multiple devices at the same time so it is important to consider cross
                    platform support so that users can be engaged with products and services according to their needs.
                </p>
                <p class="blog-body-text">
                    Hotstar can be watched on mobile, tablets, laptop & smart TV. This allows users to watch wherever
                    they want.</p>
