                <p class="blog-body-text">
                    Regardless of how excited you are to jump right in, you should always start your new projects by
                    creating a comprehensive schedule detailing the stages of project management. Clients want to
                    visualize the process of their project, having said that the timeline should not necessarily include
                    every minute detail of the project (ex. internal scheduled meetings, staff vacation days, or
                    additional buffer time).
                </p>
                <h4 class="blog-body-title">For both the internal timeline and the client timeline, you should always
                    include these points while planning the Stages Of Project Management</h4>
                <p class="blog-body-text"><b>a) Identify individual deliverables</b></p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/design-timelines-execution/project-management-stages-identify-timeline-deliverables.jpg" alt="project-management-stages-identify-timeline-deliverables" />
                </div>
                <p class="blog-body-text">
                    Begin by writing down your final deliverable (i.e. a website or app) then work your way down and
                    create a list of all sub-deliverables that need to be built to complete the final deliverable. For
                    example, if your final ui ux design deliverable is a website, your sub-deliverables could include
                    wireframes, high-fidelity prototypes, copywriting, mobile designs, and more. Listing out your
                    individual project components will help you get a better sense of the scope when designing a
                    product.


                </p>
                <p class="blog-body-text"><b>b) Determine the resources required</b></p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/design-timelines-execution/project-management-stages-determine-required-resources.jpg" alt="project-management-stages-determine-required-resources" />
                </div>
                <p class="blog-body-text">
                    Now that you have listed out the flow of your project, it’s time to forecast the amount of resources
                    you need to accomplish the project. This includes the time needed to complete each deliverable in
                    the stages of project management, as well as the individual contributors or teams you’ll need to
                    complete specific elements while designing a product.


                </p>
                <p class="blog-body-text"><b>c) Determine the outside elements that could have an impact on your
                        project</b></p>
                <div class="blog-banner-img">
