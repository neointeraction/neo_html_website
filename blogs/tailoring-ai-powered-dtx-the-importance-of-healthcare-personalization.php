                <h4 class="blog-body-title">
                    2. Why are UXUI Principles Important in Digital Therapeutics?
                </h4>

                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/tailoring-ai-powered-dtx/image2.jpg" alt="image2" />
