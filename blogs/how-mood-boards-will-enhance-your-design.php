                <h4 class="blog-body-title">How to create a mood board?</h4>

                <p class="blog-body-text">
                    There is no "One Size Fits All" to creating a mood board meaning that there are different formats
                    and styles. Cohesiveness sets the theme of the mood board as well as the final design based on it.
                    For example, a construction company website might have imagery of buildings, and abstract
                    architectural themes and colors would be well matched.
                </p>

                <p class="blog-body-text">
                    <b>Know about the project or visual design problem</b>
                </p>

                <p class="blog-body-text">
                    Understanding your audience is essential when creating a mood board so that your audience relates to
                    your brand values and products. It is critical to select the proper color for your brand in order to
                    attract customers.
                </p>

                <p class="blog-body-text">
                    <b>Set up the tone for the project</b>
                </p>

                <p class="blog-body-text">
                    Ask questions and make a list of keywords that emits the emotion of the project so that you’ll have
                    a clear idea.
                </p>

                <p class="blog-body-text">
                    <b>Gather resources based on the project idea</b>
                </p>

                <p class="blog-body-text">
                    In this phase, try to collect relevant images and elements based on the questions asked in the
                    previous step. Focus on making a collection of relatable visual representations.
                </p>

                <p class="blog-body-text">
