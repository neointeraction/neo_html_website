                </p>

                <p class="blog-body-text">
                    Most designers rush online for mood board examples and inspiration, and Google surely has a vast
                    collection of eye-catchy images but do not limit yourself to one source. Go beyond the digital
                    media, and take inspiration from the outside world.
                </p>

                <p class="blog-body-text">
                    <b>Collaborate with the design team to get more ideas from different perspectives</b>
                </p>

                <p class="blog-body-text">
                    Before collaborating with the UI UX design team, you’d need to organize your images and elements
                    accordingly. Also, this is a good chance to filter out some of those images that you might not like
                    or look a little out of the theme.<br />
                    You can take the help of google slides to organize your ideas, all you have to do is drag and drop
                    all the images and arrange them according to the story you want to tell. Then you can ask your UI UX
                    design team to come and give feedback on the mood board.
