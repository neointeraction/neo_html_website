                <h4 class="blog-body-title">
                    Structure to follow for microinteractions
                </h4>

                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-microinteractions/image2.jpg" alt="image2" />
                </div>
                

                <p class="blog-body-text">
                    Microinteractions work on a standard four steps. These are;
                </p>

                <p class="blog-body-text">
                    <b>1- Trigger: </b>
                    a trigger is the start of the micro-interactions. It can be initiated by the user or the system itself.<br/>
                    User-initiated microinteractions can be pressing a button, swiping, scrolling, or clicking; system-initiated triggers, on the other hand, happen when certain qualifications are met, and the system decides to initiate a trigger. For example, a pop-up animation, or a notification can be system triggered. Check out the microinteractions on <a href="<?php echo $path; ?>" target="_blank">Neointeraction design</a> to understand the concept better.
                </p>

                <p class="blog-body-text">
                    <b>2- Rules: </b>
                    UI UX rules determine what happens after the trigger is initiated.
                </p>

                <p class="blog-body-text">
                    <b>3- Feedback: </b>
                    is what motion happens once the trigger is initiated, basically, everything the user hears sees, or feels (e.g. vibration motion) counts as feedback. For example, when you swipe, that is a trigger; and the animation you see on the screen (changing of the colors, highlights, or the screen in an animated way) is the feedback.
                </p>

                <p class="blog-body-text">
                    <b>4- Loops & Modes:</b>
                    determine the meta-rules of the microinteraction, they are needed when the conditions of the microinteractions change. They may not be a part of the initial design, but they are essential for user satisfaction.
                </p>

               
