                <h4 class="blog-body-title">
                    What questions do you need to ask your clients?

                </h4>

                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/business-objectives-good-design/key-questions-to-ask-clients-for-ux-ui-design.jpeg" alt="blog1" />
                </div>

                <p class="blog-body-text">
                    A good customer is a valuable source of information that may help you save time and energy on a design project. It is very crucial to conduct user interviews
                    and client interviews. You should leverage their knowledge early in a project to learn the difficulties and concerns that their users are most concerned about.
                </p>


                <p class="blog-body-text">

                <ol>

                    <li>What does the product do and what do you want it to do?</li>
                    <li>What are your business objectives and how will they support your users?</li>
                    <li>Who is the audience?</li>
                    <li>How do they use the product / consume the service right now? (mobile / desktop / telephone / email / paper)</li>
                    <li>What do the users like/dislike about the product?</li>
                    <li>Are there any special needs of the user I should be aware of when designing? (areas of limited connectivity / less tech-savvy / language / desktop-only
                        etc.)</li>
                    <li>Are there any special needs of the client I should be aware of when designing? (relationship with previous contractors / sensitive language / not easily
                        available etc.)</li>
                    <li>What actions do users usually perform while using the product/service? (Usability)</li>
                    <li>How many users does this product have? Where would you like to take it? (Analytics — more users, more impressions, more page views, more conversions)</li>
                    <li>Are there areas where the users get lost right now? If I were to glance through customer support tickets, what would they be about?</li>
                    <li>What would be your elevator pitch for this product? (Client)</li>
                    <li>How would you think a user would describe this product? (User experience)</li>
                    <li>What have you already tried before and have worked / not worked for the company?</li>
                    <li>What user research has been done till now? Which techniques were used? Were they helpful? How?</li>
                    <li>What would an MVP look like?</li>

                </ol>
                </p>

                <p class="blog-body-text">
                    Business-related:
                <ul>
                    <li>What’s the scope of this project? What’s the budget like?</li>
                    <li>What are the client’s short-term and long-term goals and how much time do we have to deliver them?</li>
                    <li>What teams would we need to set up to achieve these goals?</li>
                    <li>Is there any domain-specific knowledge I should know/learn before starting on this?</li>
                    <li>What does User Experience mean for the client? How aware are they of the jargon in UX? (self-assess or ask team-member)</li>
                    <li>Is the client flexible/fastidious on the delivered solution? (self-assess or ask team-member)</li>
                    <li>For doing research, what are the policies I should be concerned about?</li>
                    <li>What techniques can I incorporate that would aid discovery (while keeping the policy in mind)?</li>
                    <li>Where in the design process are we? (Initial discovery, Strategy, Design, Testing)</li>
                    <li>What tools are the developers/other designers using?</li>
                    <li>How can the issues identified be prioritized to align with the client’s project roadmap?</li>
                    <li>What other contractor teams/client teams would we be working with? What do they do?</li>
                    <li>What has been done until now? By whom? What have you/they found from it?</li>
                    <li>What are the immediate next steps for us? Next meeting?</li>

                </ul>
