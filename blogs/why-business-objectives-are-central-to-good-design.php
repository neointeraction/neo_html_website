                </h4>
                <p class="blog-body-text">
                    <b>Listening as an Action</b>
                </p>

                <p class="blog-body-text">
                    Listening to our customers' comments needs active participation on our part as UX researchers. We engage in active listening to be ready to respond to the
                    conversation at any point. As listeners, we need to repeat back, practically verbatim, anything that we have just heard. We need to engage in listening intently
                    and attentively. To keep the conversation moving, we must ask questions quickly. An essential component of client satisfaction is communication skills.

                    We need to listen with a focused intent to make our customers feel heard and understood. Our customers should feel recognized to share anything and everything
                    about their product and user experience with us freely.</p>
