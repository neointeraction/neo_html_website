                    <b>Menus & Lists</b>
                </p>
                <p class="blog-body-text">
                    Many app menus have huge gaps between the icons and the labels. This adds mental complexity to your
                    design as the user needs to organize the interface in their head. Take care to get your proximities
                    right and the interface will be much easier to understand.

                    Every app has some sort of list screen, therefore knowing how to improve them is quite useful. The
                    purpose of list design is to make individual items easily identifiable. Instead of using lines
                    between things, you can use proximity to reduce visual noise. Just make sure there's more space
                    between items than between the components that make up an item.
