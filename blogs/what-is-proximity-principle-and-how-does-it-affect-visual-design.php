                </div>
                <p class="blog-body-text">
                    The importance of proximity in making a text easy to read cannot be overstated. A title, for
                    example, requires more white space before and after it. It feels more connected to the portion it
                    belongs to since it is closer to it. Another example is line height. The line spacing (i.e, space
                    between lines) must be larger than the space between words, but not so large that they do not
                    constitute a paragraph. The same can be said about letter spacing. The spacing must be large enough
                    to discern letters yet tiny enough to allow words to be formed from the individual letters.

                    Let's take an example of the <a href="https://www.britannica.com/" target="_blank">Britannica
                        Website</a>

                </p>
