                <h4 class="blog-body-title">
                    Top User Research Method through the design process
                </h4>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/how-to-conduct-user/image3.jpg" alt="image3" />
                </div>
                <p class="blog-body-text">
                <ol>
                    <li>
                        Discover Stage
                        <ul>
                            <li>
                                <b>Contextual Interviews : </b> Enables the observation of users in their natural
                                environment, giving you a better understanding of the way users work.
                            </li>
                            <li>
                                <b>Diary studies : </b> Have users record their daily interactions with a design or log
                                their performance of activities.
                            </li>
                            <li>
                                <b>Focus groups : </b> Participatory groups that are led through a discussion and
                                activities to gather data on a particular product or service.
                            </li>
                        </ul>
                    </li>
                    <li>
                        Explore
                        <ul>
                            <li>
                                <b>Card sorting : </b> Write words and phrases on cards; then let participants organize
                                them in the most meaningful way and label categories to ensure that your design is
                                structured in a logical way. Card Sorting: Allows users to group and sort a site’s
                                information into a logical structure that will typically drive navigation and the site’s
                                information architecture. This helps ensure that the site structure matches the way
                                users think.
                            </li>
                            <li>
                                <b>Prototyping : </b> This allows the design team to explore ideas before implementing
                                them by creating a mock-up of the site. A prototype can range from a paper mock-up to
                                interactive HTML pages.
                            </li>
                            <li>
                                <b>Customer journey maps : </b> Create user journeys to expose potential pitfalls and
                                crucial moments.
                            </li>
                        </ul>
                    </li>
                    <li>
                        Test
                        <ul>
                            <li>
                                <b>First Click Testing : </b> EA navigation-focused testing method that can be used on a
                                live website, a prototype, or a wireframe.
                            </li>
                            <li>
                                <b>Usability Testing UX : </b> Identifies user frustrations and site problems through
                                one-on-one sessions in which a "real-life" user performs tasks on the site under
                                investigation.
                            </li>
                            <li>
                                <b>Accessibility evaluations : </b> Test your design to make sure it's user-friendly.
                            </li>
                            <li>
                                <b>A/B testing : </b> Comparing two different versions of a web page to find which one
                                converts the most visitors. This is a fantastic tool to experiment with button
                                locations, colors, banners, and other UI elements.
                            </li>
                        </ul>
                    </li>
                    <li>
                        Listen
                        <ul>
                            <li>
                                <b>Surveys : </b> A survey is a type of research technique that generally consists of a
                                set of questions intended to determine your users' preferences, attitudes, and opinions
                                on a specific topic. Today, most surveys are completed online and come in a variety of
                                durations and forms. Data from surveys is automatically received, and the chosen survey
                                tool usually includes some level of analysis. This data can then be used for user
                                experience studies, later on, to help inform your product.
                            </li>
                            <li>
                                <b>Analytics : </b> Collect analytics/metrics to chart (e.g.) website traffic and build
                                reports.
                            </li>
                            <li>
                                <b>User Questionnaires : </b> Users provide feedback when they interact with your
                                product during testing or after launch to assist you to understand the app's features
                                and usability. You can use open-ended questions or multiple-choice questions to get a
                                mix of qualitative and quantitative information, which is often combined with the
                                alternatives mentioned above.
                            </li>
                        </ul>
                    </li>
                </ol>
                </p>

