                <h4 class="blog-body-title">
                    UI UX terminologies
                </h4>
                <p class="blog-body-text">
                    <b>User Experience (UX)</b>
                </p>
                <p class="blog-body-text">
                    A set of impressions and emotions the user gets from interacting with the user interface of an app
                    or a website.
                </p>
                <p class="blog-body-text">
                    <b>User Experience Design (UXD)</b>
                </p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-terminologies/user-experience-design.jpeg"
                        alt="User Experience Design" />
                </div>
                <p class="blog-body-text">
                    Designing software products and systems to be useful to a set of end-users. It is a broad concept
                    applied during the design process. UX design covers the technical use of a product or service and
                    its essential physical interface.
                </p>
                <p class="blog-body-text">
                    <b>Customer Experience</b>
                </p>
                <p class="blog-body-text">
                    Customer experience or CX, is the customers’ holistic perception of their experience with a business
                    or brand. It covers the user journey and every interaction the customer has with a business, from
                    navigating the website to talking to customer service and receiving the product /service they
                    bought. CX tends to be broader in scope than user experience (UX). UX is focused on a person’s
                    interaction with an app or website, while CX is more about that person’s overall experience of a
                    brand, including advertising, product quality, and customer support.
                </p>
                <p class="blog-body-text">
                    <b>User Interface (UI), or Graphical User Interface (GUI)</b>
                </p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-terminologies/user-interface.jpeg"
                        alt="User Interface meaning" />
                </div>
                <p class="blog-body-text">
                    What the user sees. This can be a set of commands or menus through which a user communicates with a
                    program. It is also the space where interactions between humans and machines occur.
                </p>
                <p class="blog-body-text">
                    <b>Interaction Design (IXD)</b>
                </p>
                <p class="blog-body-text">
                    Interaction Design, or IxD, is the practice of designing interactive digital products and
                    considering the way in which users will interact with them. 3-click Rule The theory is that users
                    will abandon a website if they are unable to complete their task within 3 mouse clicks.
                </p>
                <p class="blog-body-text">
                    <b>Sketching</b>
                </p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-terminologies/Sketching-meaning.jpeg"
                        alt="Sketching meaning" />
                </div>
                <p class="blog-body-text">
                    A sketch is a rough drawing. In UX design, sketching not only helps with developing user interfaces
                    but is useful for design collaboration and communicating ideas.
                </p>
                <p class="blog-body-text">
                    <b>A/B Testing</b>
                </p>
                <p class="blog-body-text">
                    A/B testing is a controlled experiment for comparing two versions of the design. The goal is to
                    identify which one is more successful. It is a way of testing the designers' hypotheses. A/B testing
                    allows them to confirm whether the hypotheses will work out well or not. It helps you with making
                    the right decisions.
                </p>
                <p class="blog-body-text">
                    <b>Accessibility</b>
                </p>
                <p class="blog-body-text">
                    Accessibility or accessible design helps differently-enabled users to interact with a product. It
                    involves designing for people who are color blind, blind, deaf, and people with cognitive
                    disabilities, among others.
                </p>
                <p class="blog-body-text">
                    <b>Adaptive</b>
                </p>
                <p class="blog-body-text">
                    An adaptive interface is a set of layouts designed specifically for different devices. It is
                    designed to detect the device type being used and display the appropriate layout for it. Different
                    versions of the same site are created, these versions are optimized to be viewed on mobiles,
                    desktops, or tablets.
                </p>
                <p class="blog-body-text">
                    <b>API</b>
                </p>
                <p class="blog-body-text">
                    This stands for Application Programming Interface. An API is how computers and web applications
                    share information with each other. Products develop APIs to let you access and read the information
                    on their server easily.
                </p>
                <div class="blog-quote-block">
                    <div class="quote-icon"></div>
                    <p class="blog-quote-text">Terminologies are important to understand the technical and scientific
                        contexts. It also helps specialists and students of the respective fields to communicate
                        effectively.</p>
                </div>
                <p class="blog-body-text">
                    <b>Flat Design</b>
                </p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-terminologies/Flat-Design-meaning.jpeg"
                        alt="Flat Design meaning" />
                </div>
                <p class="blog-body-text">
                    A design trend that followed skeuomorphism. It moved away from both the imitation of realistic
                    surfaces, and the creation of digital interface elements that realistically reference physical
                    interfaces. Instead, it favored simple blocks of flat color, making labeling and iconography more
                    prominent, and increasing the visual simplicity and clarity of interface designs.
                </p>
                <p class="blog-body-text">
                    <b>MVP</b>
                </p>
                <p class="blog-body-text">
                    MVP, or Minimal Viable Product, is a version of a product that has the minimum amount of features
                    but can be used for user testing or demonstration. This approach allows for a more elaborate,
                    in-depth testing phase before its potential is determined. MVP, or Minimal Viable Product, is a
                    version of a product that has the minimum amount of features but can be used for user testing or
                    demonstration. This approach allows for a more elaborate, in-depth testing phase before its
                    potential is determined.
                </p>
                <p class="blog-body-text">
                    <b>White space</b>
                </p>
                <p class="blog-body-text">
                    White space or negative space refers to the unoccupied or blank space on a page. In the shot below,
                    white space or negative space is used cleverly to shape a cursor.
                </p>
                <p class="blog-body-text">
                    <b>Responsive design</b>
                </p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-terminologies/Responsive-design-meaning.jpeg"
                        alt="Responsive design meaning" />
                </div>
                <p class="blog-body-text">
                    Not to be confused with adaptive, responsive websites adapt to fit the device they are being
                    displayed on. They are a single layout that shrinks and stretches and rearranges the content on it
                    to be easily viewed on each device type.
                </p>
                <p class="blog-body-text">
                    <b>Wireframe</b>
                </p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-terminologies/wireframes-meaning.jpeg"
                        alt="wireframes meaning" />
                </div>
                <p class="blog-body-text">
                    A rough version of a screen design that allows the designer to rapidly explore different options for
                    layout and visual hierarchy and gather feedback through testing. Typically, a wireframe does not
                    include any decision-making around color or typography, although wireframes can range from low- to
                    high-fidelity.
                </p>
                <p class="blog-body-text">
                    <b>Mockup</b>
                </p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-terminologies/Mockup-meaning.jpeg"
                        alt="Mockup meaning" />
                </div>
                <p class="blog-body-text">
                    A mockup is a detailed static representation of the design that clearly displays the information
                    structure, renders the content, and demonstrates the basic functionality.
                </p>
                <p class="blog-body-text">
                    <b>Prototype</b>
                </p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-terminologies/Prototype-meaning.jpeg"
                        alt="Prototype meaning" />
                </div>
                <p class="blog-body-text">
                    A prototype is a simulation or sample version of a final product, which is used for testing prior to
                    launch. Its goal is to test products (and product ideas) before sinking lots of time and money into
                    the final product.
                </p>
                <p class="blog-body-text">
                    <b>Affordance</b>
                </p>
                <p class="blog-body-text">
                    An adaptive interface is a set of layouts designed specifically for different devices. It is
                    designed to detect the device type being used and display the appropriate layout for it. Different
                    versions of the same site are created, these versions are optimized to be viewed on mobiles,
                    desktops, or tablets.
                </p>
                <p class="blog-body-text">
                    <b>Clickstream</b>
                </p>
                <p class="blog-body-text">
                    Clickstream is a way of collecting and analyzing data to track users’ actions. It usually takes
                    place during user interactions with the product. It is also a method to get unbiased quantitative
                    data about user behavior.
                </p>
                <p class="blog-body-text">
                    <b>Card Sorting</b>
                </p>
                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/ui-ux-design-terminologies/Card-Sorting-meaning.jpeg"
                        alt="Card Sorting meaning" />
                </div>
                <p class="blog-body-text">
                    The goal of card sorting is to understand how a typical user views a given set of items. UI UX
                    Designers write items on individual paper cards and then ask users to group together similar cards.
                    Card sorting helps to create websites that are easy to navigate.
                </p>
                <p class="blog-body-text">
                    <b>KPI — Key Performance Indicator</b>
                </p>
                <p class="blog-body-text">
                    Key performance indicators are the metrics that are most important to a business or project. "KPIs"
                    are used to track progress and measure goals.
                </p>
