                <h4 class="blog-body-title">Different types of wireframes</h4>

                <div class="blog-banner-img">
                    <img src="<?php echo $path; ?>assets/images/blog/what-are-wireframes-and-why/different-types-of-wireframes-in-ux-ui-design.jpg"
                        alt="different-types-of-wireframes-in-ux-ui-design" />
                </div>


                <p class="blog-body-text">
                    <b>Low Fidelity Wireframes</b>
                    Low fidelity wireframes are usually static and contain only the basic content and images. Their
                    function is to describe the hierarchy and structure of a page, and they are often presented as
                    simple, hand-drawn sketches depicting initial layout concepts utilizing various forms. With
                    low-fidelity wireframes, we aim to cover a variety of stakeholder goals, user engagements, and user
                    demands. They're also easy to change because they're crude sketches. Pen and paper sketches are the
                    most frequent way to create low-fidelity wireframe designs.
                </p>

                <p class="blog-body-text">
                    <b>Mid Fidelity Wireframes</b>
                    In contrast to low-fidelity wireframes, medium-fidelity wireframes are created using software tools.
                    They are usually created in a monochrome palette, instead of black and white, which displays
                    visually in grayscale colors. We can design medium-fidelity wireframes using either a ready-made
                    tool designed specifically for wireframes or by hand using Sketch or a similar tool. Mid-fidelity
                    wireframes begin to describe the various UI elements for sections with an emphasis on functionality
                    and visual hierarchy, in addition to further developing concepts for overall content and
                    organization.
                </p>

                <p class="blog-body-text">
                    <b>High Fidelity Wireframes</b>
                    High-fidelity wireframes further build upon the details of mid-fidelity wireframes with a clear
                    focus on usability and design. It can include typography, colors, images, icons, and CTA buttons.
                    These types of wireframes take longer than the low and medium-fidelity kinds which means more
                    resources are usually allocated to complete them. These wireframes help in ux testing with realistic
                    representations of what the product will look and feel like, as well as how it will respond to
                    them.High-fidelity wireframes should be reserved for projects that have been approved by all
                    stakeholders and are cleared for production. UI UX Designers usually use different wireframe tools
                    depending on what kind of wireframe they want to build â€“ ranging from low to medium to high
                    fidelity.


                </p>



