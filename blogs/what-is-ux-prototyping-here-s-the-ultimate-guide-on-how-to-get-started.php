                    An animated prototype is an invaluable tool used by designers to create a rough run-through of an
                    app or interface that shows the transitions and motion of a design. While some mockups can convey
                    the overall style of a user interface, an animated prototype offers an active simulation.
                </p>

                <p class="blog-body-text">
                    Animations are advantageous for UI UX developers because you can look at your prototypes as one of
                    your best tools of communication. By prototyping animations, and sharing them with your UI UX
                    developers, they will be able to see exactly how you imagined the product. The same can be said for
                    any stakeholders. They’ll be able to see moveable screens even when there is no existing product.
                    You don’t have to write thousands of papers filled with detailed instructions about the features and
                    movements of the animations.
