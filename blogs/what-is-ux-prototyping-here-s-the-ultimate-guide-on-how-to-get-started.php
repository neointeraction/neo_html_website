                <h4 class="blog-body-title">
                    Types of Prototyping
                </h4>

                <p class="blog-body-text">

                    Teams create prototypes with varying degrees of detail in order to capture design thoughts and test
                    them on users. The level of fidelity you select should be suitable for presenting to users in user
                    testing so that they may provide focused feedback.

                </p>

                <div class="blog-banner-img">
